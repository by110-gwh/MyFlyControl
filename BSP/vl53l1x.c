#include "vl53l1x.h"
#include "vl53l1_api.h"

//VL53L1X设备句柄
static VL53L1_Dev_t dev;

/**********************************************************************************************************
*函 数 名: vl53l1x_init
*功能说明: VL53L01X激光测距初始化
*形    参: 无
*返 回 值: 无
**********************************************************************************************************/
void vl53l1x_init()
{
    VL53L1_WaitDeviceBooted(&dev);
    VL53L1_DataInit(&dev);
    VL53L1_StaticInit(&dev);
}
